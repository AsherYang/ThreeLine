    def __init__(self):
        pass

    @property
    def date(self):
        return self.date

    @property
    def date(self, value):
        self.date = value

    @property
    def day(self):
        return self.day

    @property
    def day(self, value):
        self.day = value

    @property
    def hours(self):
        return self.hours

    @property
    def hours(self, value):
        self.hours = value

    @property
    def minutes(self):
        return self.minutes

    @property
    def minutes(self, value):
        self.minutes = value

    @property
    def month(self):
        return self.month

    @property
    def month(self, value):
        self.month = value

    @property
    def nanos(self):
        return self.nanos

    @property
    def nanos(self, value):
        self.nanos = value

    @property
    def seconds(self):
        return self.seconds

    @property
    def seconds(self, value):
        self.seconds = value

    @property
    def time(self):
        return self.time

    @property
    def time(self, value):
        self.time = value

    @property
    def timezoneOffset(self):
        return self.timezoneOffset

    @property
    def timezoneOffset(self, value):
        self.timezoneOffset = value

    @property
    def year(self):
        return self.year

    @property
    def year(self, value):
        self.year = value
